
public class DadosGerais {
	private String nome;
	private String nomeEmCitacoes;
	private String nacionalidade;
	private String paisNasc;
	private String ufNasc;
	private String cidadeNasc;
	private String permissaoDivulg;
	private String dataFalecimento;
	private String siglaPaisNacionalidade;
	
	public DadosGerais(String nome, String nomeEmCitacoes, String nacionalidade, String paisNasc, String ufNasc,
			String cidadeNasc, String permissaoDivulg, String dataFalecimento, String siglaPaisNacionalidade,
			String paisNacionalidade) {
		super();
		this.nome = nome;
		this.nomeEmCitacoes = nomeEmCitacoes;
		this.nacionalidade = nacionalidade;
		this.paisNasc = paisNasc;
		this.ufNasc = ufNasc;
		this.cidadeNasc = cidadeNasc;
		this.permissaoDivulg = permissaoDivulg;
		this.dataFalecimento = dataFalecimento;
		this.siglaPaisNacionalidade = siglaPaisNacionalidade;
		this.paisNacionalidade = paisNacionalidade;
	}
	
	public DadosGerais() {
		super();
		this.nome = nome;
		this.nomeEmCitacoes = nomeEmCitacoes;
		this.nacionalidade = nacionalidade;
		this.paisNasc = paisNasc;
		this.ufNasc = ufNasc;
		this.cidadeNasc = cidadeNasc;
		this.permissaoDivulg = permissaoDivulg;
		this.dataFalecimento = dataFalecimento;
		this.siglaPaisNacionalidade = siglaPaisNacionalidade;
		this.paisNacionalidade = paisNacionalidade;
	}
	private String paisNacionalidade;
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getNomeEmCitacoes() {
		return nomeEmCitacoes;
	}
	public void setNomeEmCitacoes(String nomeEmCitacoes) {
		this.nomeEmCitacoes = nomeEmCitacoes;
	}
	public String getNacionalidade() {
		return nacionalidade;
	}
	public void setNacionalidade(String nacionalidade) {
		this.nacionalidade = nacionalidade;
	}
	public String getPaisNasc() {
		return paisNasc;
	}
	public void setPaisNasc(String paisNasc) {
		this.paisNasc = paisNasc;
	}
	public String getUfNasc() {
		return ufNasc;
	}
	public void setUfNasc(String ufNasc) {
		this.ufNasc = ufNasc;
	}
	public String getCidadeNasc() {
		return cidadeNasc;
	}
	public void setCidadeNasc(String cidadeNasc) {
		this.cidadeNasc = cidadeNasc;
	}
	public String getPermissaoDivulg() {
		return permissaoDivulg;
	}
	public void setPermissaoDivulg(String permissaoDivulg) {
		this.permissaoDivulg = permissaoDivulg;
	}
	public String getDataFalecimento() {
		return dataFalecimento;
	}
	public void setDataFalecimento(String dataFalecimento) {
		this.dataFalecimento = dataFalecimento;
	}
	public String getSiglaPaisNacionalidade() {
		return siglaPaisNacionalidade;
	}
	public void setSiglaPaisNacionalidade(String siglaPaisNacionalidade) {
		this.siglaPaisNacionalidade = siglaPaisNacionalidade;
	}
	public String getPaisNacionalidade() {
		return paisNacionalidade;
	}
	public void setPaisNacionalidade(String paisNacionalidade) {
		this.paisNacionalidade = paisNacionalidade;
	}
}

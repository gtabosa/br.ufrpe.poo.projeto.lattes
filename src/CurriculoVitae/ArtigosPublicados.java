package CurriculoVitae;

public abstract class ArtigosPublicados {
	
	int sequenciaProducao;
	String ordemImportancia;
	
	
	public int getSequenciaProducao() {
		return sequenciaProducao;
	}
	public void setSequenciaProducao(int sequenciaProducao) {
		this.sequenciaProducao = sequenciaProducao;
	}
	public String getOrdemImportancia() {
		return ordemImportancia;
	}
	public void setOrdemImportancia(String ordemImportancia) {
		this.ordemImportancia = ordemImportancia;
	}	
}

package CurriculoVitae;

public class ParticipanteBanca{
	
	private String nomeCompletoParticipanteBanca;
	private String nomeParaCitacaoParticipanteBanca;
	private int ordemParticipante;
	private int nroIdCnpq;
	
	
	/**
	 * @return the nomeCompletoParticipanteBanca
	 */
	public String getNomeCompletoParticipanteBanca() {
		return nomeCompletoParticipanteBanca;
	}
	/**
	 * @param nomeCompletoParticipanteBanca the nomeCompletoParticipanteBanca to set
	 */
	public void setNomeCompletoParticipanteBanca(String nomeCompletoParticipanteBanca) {
		this.nomeCompletoParticipanteBanca = nomeCompletoParticipanteBanca;
	}
	/**
	 * @return the nomeParaCitacaoParticipanteBanca
	 */
	public String getNomeParaCitacaoParticipanteBanca() {
		return nomeParaCitacaoParticipanteBanca;
	}
	/**
	 * @param nomeParaCitacaoParticipanteBanca the nomeParaCitacaoParticipanteBanca to set
	 */
	public void setNomeParaCitacaoParticipanteBanca(String nomeParaCitacaoParticipanteBanca) {
		this.nomeParaCitacaoParticipanteBanca = nomeParaCitacaoParticipanteBanca;
	}
	/**
	 * @return the ordemParticipante
	 */
	public int getOrdemParticipante() {
		return ordemParticipante;
	}
	/**
	 * @param ordemParticipante the ordemParticipante to set
	 */
	public void setOrdemParticipante(int ordemParticipante) {
		this.ordemParticipante = ordemParticipante;
	}
	/**
	 * @return the nroIdCnpq
	 */
	public int getNroIdCnpq() {
		return nroIdCnpq;
	}
	/**
	 * @param nroIdCnpq the nroIdCnpq to set
	 */
	public void setNroIdCnpq(int nroIdCnpq) {
		this.nroIdCnpq = nroIdCnpq;
	}
	
}

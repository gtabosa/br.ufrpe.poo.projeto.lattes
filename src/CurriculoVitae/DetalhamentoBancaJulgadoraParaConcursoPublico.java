package CurriculoVitae;

public class DetalhamentoBancaJulgadoraParaConcursoPublico extends ParticipacaoBancaJulgadora{
	
	private int codInstituicao;
	private String nomeInstiruicao;
	
	
	/**
	 * @return the codInstituicao
	 */
	public int getCodInstituicao() {
		return codInstituicao;
	}
	/**
	 * @param codInstituicao the codInstituicao to set
	 */
	public void setCodInstituicao(int codInstituicao) {
		this.codInstituicao = codInstituicao;
	}
	/**
	 * @return the nomeInstiruicao
	 */
	public String getNomeInstiruicao() {
		return nomeInstiruicao;
	}
	/**
	 * @param nomeInstiruicao the nomeInstiruicao to set
	 */
	public void setNomeInstiruicao(String nomeInstiruicao) {
		this.nomeInstiruicao = nomeInstiruicao;
	}
	
}

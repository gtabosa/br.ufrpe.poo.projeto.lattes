package CurriculoVitae;

public class ParticipanteEvetosCongresso extends ParticipacaoEmCongresso{
	
	private String nomeCompletoParticipante;
	private String nomeParaCitacaoParticipante;
	private int ordemParticipante;
	private int nroIdCnpq;
	
	
	/**
	 * @return the nomeCompletoParticipante
	 */
	public String getNomeCompletoParticipante() {
		return nomeCompletoParticipante;
	}
	/**
	 * @param nomeCompletoParticipante the nomeCompletoParticipante to set
	 */
	public void setNomeCompletoParticipante(String nomeCompletoParticipante) {
		this.nomeCompletoParticipante = nomeCompletoParticipante;
	}
	/**
	 * @return the nomeParaCitacaoParticipante
	 */
	public String getNomeParaCitacaoParticipante() {
		return nomeParaCitacaoParticipante;
	}
	/**
	 * @param nomeParaCitacaoParticipante the nomeParaCitacaoParticipante to set
	 */
	public void setNomeParaCitacaoParticipante(String nomeParaCitacaoParticipante) {
		this.nomeParaCitacaoParticipante = nomeParaCitacaoParticipante;
	}
	/**
	 * @return the ordemParticipante
	 */
	public int getOrdemParticipante() {
		return ordemParticipante;
	}
	/**
	 * @param ordemParticipante the ordemParticipante to set
	 */
	public void setOrdemParticipante(int ordemParticipante) {
		this.ordemParticipante = ordemParticipante;
	}
	/**
	 * @return the nroIdCnpq
	 */
	public int getNroIdCnpq() {
		return nroIdCnpq;
	}
	/**
	 * @param nroIdCnpq the nroIdCnpq to set
	 */
	public void setNroIdCnpq(int nroIdCnpq) {
		this.nroIdCnpq = nroIdCnpq;
	}
	
}

package CurriculoVitae;

public class Idioma {
	
	private String idioma;
	private String nomeIdioma;
	private String leitura;
	private String fala;
	private String escrita;
	private String compreensão;
	
	
	public String getIdioma() {
		return idioma;
	}
	public void setIdioma(String idioma) {
		this.idioma = idioma;
	}
	public String getNomeIdioma() {
		return nomeIdioma;
	}
	public void setNomeIdioma(String nomeIdioma) {
		this.nomeIdioma = nomeIdioma;
	}
	public String getLeitura() {
		return leitura;
	}
	public void setLeitura(String leitura) {
		this.leitura = leitura;
	}
	public String getFala() {
		return fala;
	}
	public void setFala(String fala) {
		this.fala = fala;
	}
	public String getEscrita() {
		return escrita;
	}
	public void setEscrita(String escrita) {
		this.escrita = escrita;
	}
	public String getCompreensão() {
		return compreensão;
	}
	public void setCompreensão(String compreensão) {
		this.compreensão = compreensão;
	}
	
}

package CurriculoVitae;

public class AtuacaoProfissional {
	
	private int codInstituicao;
	private String nomeInstituicao;
	private int sequenciaAtividade;
	private int sequenciaImportancia;
	public int getCodInstituicao() {
		return codInstituicao;
	}
	public void setCodInstituicao(int codInstituicao) {
		this.codInstituicao = codInstituicao;
	}
	public String getNomeInstituicao() {
		return nomeInstituicao;
	}
	public void setNomeInstituicao(String nomeInstituicao) {
		this.nomeInstituicao = nomeInstituicao;
	}
	public int getSequenciaAtividade() {
		return sequenciaAtividade;
	}
	public void setSequenciaAtividade(int sequenciaAtividade) {
		this.sequenciaAtividade = sequenciaAtividade;
	}
	public int getSequenciaImportancia() {
		return sequenciaImportancia;
	}
	public void setSequenciaImportancia(int sequenciaImportancia) {
		this.sequenciaImportancia = sequenciaImportancia;
	}
	
	
	
}

package CurriculoVitae;

public class DetalhamentoSoftware extends ProducaoTecnicaSoftware{
	
	private String finalidade;
	private String plataforma;
	private String ambiente;
	private String instituicaoFinanciadora;
	private String finalidade_EN;
	
	
	/**
	 * @return the finalidade
	 */
	public String getFinalidade() {
		return finalidade;
	}
	/**
	 * @param finalidade the finalidade to set
	 */
	public void setFinalidade(String finalidade) {
		this.finalidade = finalidade;
	}
	/**
	 * @return the plataforma
	 */
	public String getPlataforma() {
		return plataforma;
	}
	/**
	 * @param plataforma the plataforma to set
	 */
	public void setPlataforma(String plataforma) {
		this.plataforma = plataforma;
	}
	/**
	 * @return the ambiente
	 */
	public String getAmbiente() {
		return ambiente;
	}
	/**
	 * @param ambiente the ambiente to set
	 */
	public void setAmbiente(String ambiente) {
		this.ambiente = ambiente;
	}
	/**
	 * @return the instituicaoFinanciadora
	 */
	public String getInstituicaoFinanciadora() {
		return instituicaoFinanciadora;
	}
	/**
	 * @param instituicaoFinanciadora the instituicaoFinanciadora to set
	 */
	public void setInstituicaoFinanciadora(String instituicaoFinanciadora) {
		this.instituicaoFinanciadora = instituicaoFinanciadora;
	}
	/**
	 * @return the finalidade_EN
	 */
	public String getFinalidade_EN() {
		return finalidade_EN;
	}
	/**
	 * @param finalidade_EN the finalidade_EN to set
	 */
	public void setFinalidade_EN(String finalidade_EN) {
		this.finalidade_EN = finalidade_EN;
	}
	
}

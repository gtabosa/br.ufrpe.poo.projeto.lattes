package CurriculoVitae;

public class DetalhamentoApresentacaoTrabalho extends DemaisTiposProducaoTecnica{
	
	private String nomeEvento;
	private String instituicaoPromotora;
	private String localApresentacao;
	private String cidadeApresentacao;
	private String nomeEvento_EN;
	
	
	/**
	 * @return the nomeEvento
	 */
	public String getNomeEvento() {
		return nomeEvento;
	}
	/**
	 * @param nomeEvento the nomeEvento to set
	 */
	public void setNomeEvento(String nomeEvento) {
		this.nomeEvento = nomeEvento;
	}
	/**
	 * @return the instituicaoPromotora
	 */
	public String getInstituicaoPromotora() {
		return instituicaoPromotora;
	}
	/**
	 * @param instituicaoPromotora the instituicaoPromotora to set
	 */
	public void setInstituicaoPromotora(String instituicaoPromotora) {
		this.instituicaoPromotora = instituicaoPromotora;
	}
	/**
	 * @return the localApresentacao
	 */
	public String getLocalApresentacao() {
		return localApresentacao;
	}
	/**
	 * @param localApresentacao the localApresentacao to set
	 */
	public void setLocalApresentacao(String localApresentacao) {
		this.localApresentacao = localApresentacao;
	}
	/**
	 * @return the cidadeApresentacao
	 */
	public String getCidadeApresentacao() {
		return cidadeApresentacao;
	}
	/**
	 * @param cidadeApresentacao the cidadeApresentacao to set
	 */
	public void setCidadeApresentacao(String cidadeApresentacao) {
		this.cidadeApresentacao = cidadeApresentacao;
	}
	/**
	 * @return the nomeEvento_EN
	 */
	public String getNomeEvento_EN() {
		return nomeEvento_EN;
	}
	/**
	 * @param nomeEvento_EN the nomeEvento_EN to set
	 */
	public void setNomeEvento_EN(String nomeEvento_EN) {
		this.nomeEvento_EN = nomeEvento_EN;
	}
}

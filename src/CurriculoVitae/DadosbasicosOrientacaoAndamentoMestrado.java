package CurriculoVitae;

import java.util.Date;

public class DadosbasicosOrientacaoAndamentoMestrado extends OrientacoesEmAndamento{
	
	private String natureza;
	private String tipo;
	private String tituloTrabalho;
	private Date ano;
	private String pais;
	private String idioma;
	private String homePage;
	private String DOI;
	private String tituloTrabalho_EN;
	
	
	/**
	 * @return the natureza
	 */
	public String getNatureza() {
		return natureza;
	}
	/**
	 * @param natureza the natureza to set
	 */
	public void setNatureza(String natureza) {
		this.natureza = natureza;
	}
	/**
	 * @return the tipo
	 */
	public String getTipo() {
		return tipo;
	}
	/**
	 * @param tipo the tipo to set
	 */
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	/**
	 * @return the tituloTrabalho
	 */
	public String getTituloTrabalho() {
		return tituloTrabalho;
	}
	/**
	 * @param tituloTrabalho the tituloTrabalho to set
	 */
	public void setTituloTrabalho(String tituloTrabalho) {
		this.tituloTrabalho = tituloTrabalho;
	}
	/**
	 * @return the ano
	 */
	public Date getAno() {
		return ano;
	}
	/**
	 * @param ano the ano to set
	 */
	public void setAno(Date ano) {
		this.ano = ano;
	}
	/**
	 * @return the pais
	 */
	public String getPais() {
		return pais;
	}
	/**
	 * @param pais the pais to set
	 */
	public void setPais(String pais) {
		this.pais = pais;
	}
	/**
	 * @return the idioma
	 */
	public String getIdioma() {
		return idioma;
	}
	/**
	 * @param idioma the idioma to set
	 */
	public void setIdioma(String idioma) {
		this.idioma = idioma;
	}
	/**
	 * @return the homePage
	 */
	public String getHomePage() {
		return homePage;
	}
	/**
	 * @param homePage the homePage to set
	 */
	public void setHomePage(String homePage) {
		this.homePage = homePage;
	}
	/**
	 * @return the dOI
	 */
	public String getDOI() {
		return DOI;
	}
	/**
	 * @param dOI the dOI to set
	 */
	public void setDOI(String dOI) {
		DOI = dOI;
	}
	/**
	 * @return the tituloTrabalho_EN
	 */
	public String getTituloTrabalho_EN() {
		return tituloTrabalho_EN;
	}
	/**
	 * @param tituloTrabalho_EN the tituloTrabalho_EN to set
	 */
	public void setTituloTrabalho_EN(String tituloTrabalho_EN) {
		this.tituloTrabalho_EN = tituloTrabalho_EN;
	}
	
	
}

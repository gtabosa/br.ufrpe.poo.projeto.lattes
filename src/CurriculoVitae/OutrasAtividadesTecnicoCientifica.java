package CurriculoVitae;

import java.util.Date;

public class OutrasAtividadesTecnicoCientifica extends AtuacaoProfissional{
	
	private int sequenciaFuncaoAtividade;
	private String flagPeriodo;
	private Date mesInicio;
	private Date anoInicio;
	private Date mesFim;
	private Date anoFim;
	private int codUnidade;
	private String atividadeRealizada;
	private String nomeUnidade;
	
	
	
	public int getSequenciaFuncaoAtividade() {
		return sequenciaFuncaoAtividade;
	}
	public void setSequenciaFuncaoAtividade(int sequenciaFuncaoAtividade) {
		this.sequenciaFuncaoAtividade = sequenciaFuncaoAtividade;
	}
	public String getFlagPeriodo() {
		return flagPeriodo;
	}
	public void setFlagPeriodo(String flagPeriodo) {
		this.flagPeriodo = flagPeriodo;
	}
	public Date getMesInicio() {
		return mesInicio;
	}
	public void setMesInicio(Date mesInicio) {
		this.mesInicio = mesInicio;
	}
	public Date getAnoInicio() {
		return anoInicio;
	}
	public void setAnoInicio(Date anoInicio) {
		this.anoInicio = anoInicio;
	}
	public Date getMesFim() {
		return mesFim;
	}
	public void setMesFim(Date mesFim) {
		this.mesFim = mesFim;
	}
	public Date getAnoFim() {
		return anoFim;
	}
	public void setAnoFim(Date anoFim) {
		this.anoFim = anoFim;
	}
	public int getCodUnidade() {
		return codUnidade;
	}
	public void setCodUnidade(int codUnidade) {
		this.codUnidade = codUnidade;
	}
	public String getAtividadeRealizada() {
		return atividadeRealizada;
	}
	public void setAtividadeRealizada(String atividadeRealizada) {
		this.atividadeRealizada = atividadeRealizada;
	}
	public String getNomeUnidade() {
		return nomeUnidade;
	}
	public void setNomeUnidade(String nomeUnidade) {
		this.nomeUnidade = nomeUnidade;
	}
	
	
	
	
}

package CurriculoVitae;

public class InformacoesAdicionaisInstituicao {
	
	private int codInstituicao;
	private String siglaInstituicao;
	private String siglaUfInstituicao;
	private String siglaPaisInstituicao;
	private String nomePaisInstituicao;
	private boolean flagAgenciaFomento;
	private boolean flagIntituicaoEnsino;
	
	
	/**
	 * @return the codInstituicao
	 */
	public int getCodInstituicao() {
		return codInstituicao;
	}
	/**
	 * @param codInstituicao the codInstituicao to set
	 */
	public void setCodInstituicao(int codInstituicao) {
		this.codInstituicao = codInstituicao;
	}
	/**
	 * @return the siglaInstituicao
	 */
	public String getSiglaInstituicao() {
		return siglaInstituicao;
	}
	/**
	 * @param siglaInstituicao the siglaInstituicao to set
	 */
	public void setSiglaInstituicao(String siglaInstituicao) {
		this.siglaInstituicao = siglaInstituicao;
	}
	/**
	 * @return the siglaUfInstituicao
	 */
	public String getSiglaUfInstituicao() {
		return siglaUfInstituicao;
	}
	/**
	 * @param siglaUfInstituicao the siglaUfInstituicao to set
	 */
	public void setSiglaUfInstituicao(String siglaUfInstituicao) {
		this.siglaUfInstituicao = siglaUfInstituicao;
	}
	/**
	 * @return the siglaPaisInstituicao
	 */
	public String getSiglaPaisInstituicao() {
		return siglaPaisInstituicao;
	}
	/**
	 * @param siglaPaisInstituicao the siglaPaisInstituicao to set
	 */
	public void setSiglaPaisInstituicao(String siglaPaisInstituicao) {
		this.siglaPaisInstituicao = siglaPaisInstituicao;
	}
	/**
	 * @return the nomePaisInstituicao
	 */
	public String getNomePaisInstituicao() {
		return nomePaisInstituicao;
	}
	/**
	 * @param nomePaisInstituicao the nomePaisInstituicao to set
	 */
	public void setNomePaisInstituicao(String nomePaisInstituicao) {
		this.nomePaisInstituicao = nomePaisInstituicao;
	}
	/**
	 * @return the flagAgenciaFomento
	 */
	public boolean isFlagAgenciaFomento() {
		return flagAgenciaFomento;
	}
	/**
	 * @param flagAgenciaFomento the flagAgenciaFomento to set
	 */
	public void setFlagAgenciaFomento(boolean flagAgenciaFomento) {
		this.flagAgenciaFomento = flagAgenciaFomento;
	}
	/**
	 * @return the flagIntituicaoEnsino
	 */
	public boolean isFlagIntituicaoEnsino() {
		return flagIntituicaoEnsino;
	}
	/**
	 * @param flagIntituicaoEnsino the flagIntituicaoEnsino to set
	 */
	public void setFlagIntituicaoEnsino(boolean flagIntituicaoEnsino) {
		this.flagIntituicaoEnsino = flagIntituicaoEnsino;
	}
	
	
}
